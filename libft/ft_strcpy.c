char	*ft_strcpy(char *dest, const char *src)
{
	int i;

	if (src[0] == '\0')
	{
		dest[0] = src[0];
		return (dest);
	}
	i = 1;
	dest[0] = src[0];
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
