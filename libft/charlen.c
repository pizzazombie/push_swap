#include "libft.h"

int	charlen(int n)
{
	int i;

	i = 1;
	if (n < 0)
	{
		i++;
		n = n * (-1);
	}
	while (n >= 10)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
