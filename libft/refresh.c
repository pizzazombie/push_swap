#include "libft.h"

int	refresh(char **p, int size, int i)
{
	int k;

	k = 0;
	p[i] = (char *)malloc((sizeof(**p) * (size + 1)));
	if (p[i] == NULL)
	{
		while (k <= i)
		{
			free(p[k]);
			k++;
		}
		free(p);
		return (0);
	}
	return (1);
}
