#include "libft.h"

void	ft_putnbr(int n)
{
	int j;

	j = 1000000000;
	if (n == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		n = 147483648;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = n * (-1);
	}
	while (((n / j) == 0) && (j != 1))
		j = j / 10;
	if (n >= 0)
	{
		while (j >= 1)
		{
			ft_putchar((n / j) + '0');
			n = n % j;
			j = j / 10;
		}
	}
}
