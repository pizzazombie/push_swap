#include "libft.h"

int	words(char const *st, char c)
{
	int		i;
	int		w;
	char	*s;

	s = (char *)st;
	i = 0;
	w = 0;
	while (s[i] == c)
		i++;
	while (s[i] != '\0')
	{
		if (s[i] != c && s[i] != '\0')
		{
			w++;
			while (s[i] != c && s[i] != '\0')
				i++;
		}
		while (s[i] == c)
			i++;
	}
	return (w);
}
